/// {@template app_spacing}
/// Class contains all space values used in the app,
/// regardless of whether they are vertical or horizontal.
/// {@endtemplate}
class AppSpacing {
  AppSpacing._();

  /// No spacing.
  static const none = 0.0;

  /// Extra extra small spacing of 2.0.
  static const xxs = 2.0;

  /// Extra small spacing of 4.0.
  static const xs = 4.0;

  /// Small spacing of 6.0.
  static const sm = 6.0;

  /// Medium spacing of 8.0.
  static const md = 8.0;

  /// Large spacing of 12.0.
  static const lg = 12.0;

  /// Extra large spacing of 16.0.
  static const xl = 16.0;

  /// Extra extra large spacing of 24.0.
  static const xxl = 24.0;

  /// Huge spacing of 32.0.
  static const huge = 32.0;

  /// Maximum spacing of 48.0.
  static const max = 48.0;
}
